public class Strings
{
  
  public static String tickit(String s) {
    return "`" + s + "'";
  }
  
  public static String sqit(String s) {
    return "'" + s + "'";
  }
  
  public static String dqit(String s) {
    return "\"" + s + "\"";
  }
  
  public static String parit(String s) {
    return "(" + s + ")";
  }
  
  public static String curlit(String s) {
    return "{" + s + "}";
  }
  
  public static String squareit(String s) {
    return "[" + s + "]";
  }
  
}

